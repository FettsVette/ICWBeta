function GCSetup(gcConfig)
    local allPlanets = FindPlanet.Get_All_Planets()
    local planet = nil
    local eraConfig = nil
    local eraEntry = nil
    for _, planetObj in pairs(allPlanets) do
        planet = planetObj.Get_Type().Get_Name()
		eraConfig = gcConfig[planet]
		DebugMessage("Trying to spawn stuff on %s", tostring(planet))
        if eraConfig then
            eraEntry = GetEraEntry(eraConfig)
            SetUpPlanet(planetObj, eraEntry)
        end
    end
end

function GetEraEntry(eraConfig)
    local tech = Find_Player("Empire").Get_Tech_Level()
    local eraEntry = eraConfig[tech]
    while eraEntry.UseEraConfig do
        eraEntry = eraConfig[eraEntry.UseEraConfig]
    end
    return eraEntry
end

function SetUpPlanet(planet, eraEntry)
    local owner = Find_Player(eraEntry.Owner)
    planet.Change_Owner(owner)
    if owner == Find_Player("Neutral") or not eraEntry.StartingForces then
        return
	end
	-- local spawnList = flatten(eraEntry.StartingForces)
	printTable(flatten(eraEntry.StartingForces))
    -- SpawnList(flatten(eraEntry.StartingForces), planet, owner, not owner.Is_Human(), false)
    for _, unit in pairs(flatten(eraEntry.StartingForces)) do
		if not Find_Object_Type(unit) then
			DebugMessage("%s -- unit named %s not found!", tostring(Script), tostring(unit))
		end
		units = Spawn_Unit(Find_Object_Type(unit), planet, owner)
		if units ~= nil then
			for _, u in pairs(units) do
				u.Prevent_AI_Usage(false)
			end
		end
    end
end


function listOf(num, object)
    local tab = {}
    for _=1, num do
        table.insert(tab, object)
    end
    return tab
end

function flatten(tab, res)
    local result = res or {}
    for _, entry in pairs(tab) do
        if type(entry) == "table" then
            flatten(entry, result)
        else
            table.insert(result, entry)
        end
    end
    return result
end

function printTable(tab)
	local tabStr = "Entries in table: "
	for _, entry in pairs(tab) do
		tabStr = tabStr..tostring(entry)..", "
	end
	DebugMessage(tabStr)
end